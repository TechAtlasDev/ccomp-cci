#include <iostream>

using namespace std;

int var1 = 20;
int var2 = 30;
int resultado = 0;

int main() {
    // SUMA
    resultado = var1 + var2;
    cout << "La suma de esas dos variables es: " << resultado << endl;

    // RESTA
    resultado = var1 - var2;
    cout << "La resta de esas dos variables es: " << resultado << endl;

    // Multiplicación
    resultado = var1 * var2;
    cout << "El producto de esas dos variables es: " << resultado << endl;

    // Division entera
    resultado = var1 / var2;
    cout << "La division entera de esas dos variables es: " << resultado << endl;
}
